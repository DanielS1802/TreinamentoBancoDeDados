CREATE TABLE CLIENTES (
    CODIGO          INTEGER NOT NULL,
    NOME            VARCHAR(80) NOT NULL,
    PESSOAJURIDICA  VARCHAR(1),
    CNPJCPF         VARCHAR(18),
    ENDERECO        VARCHAR(80),
    BAIRRO          VARCHAR(80),
    CIDADE          VARCHAR(80),
    UF              VARCHAR(2),
    CEP             VARCHAR(10),
    TELEFONE        VARCHAR(20),
    DATANASCIMENTO  DATE,
    SEXO            VARCHAR(1)
);

ALTER TABLE CLIENTES ADD CONSTRAINT UNQ1_CLIENTES UNIQUE (CNPJCPF);

ALTER TABLE CLIENTES ADD CONSTRAINT PK_CLIENTES PRIMARY KEY (CODIGO);

CREATE TABLE PRODUTOS (
    CODIGO             INTEGER NOT NULL,
    DESCRICAO          VARCHAR(80) NOT NULL,
    DATAAQUISICAO      DATE,
    QUANTIDADEESTOQUE  INTEGER,
    VALORCUSTO         NUMERIC(15,2),
    PERCENTUALLUCRO    NUMERIC(15,2)
);

ALTER TABLE PRODUTOS ADD CONSTRAINT PK_PRODUTOS PRIMARY KEY (CODIGO);

CREATE TABLE ESTOQUE (
    CODIGOPRODUTO  INTEGER NOT NULL,
    DATA           DATE NOT NULL,
    QUANTIDADE     INTEGER,
    VALORCUSTO     NUMERIC(15,2)
);

ALTER TABLE ESTOQUE ADD CONSTRAINT PK_ESTOQUE PRIMARY KEY (CODIGOPRODUTO, DATA);